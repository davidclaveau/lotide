const findKey = (object, callback) => {
  const keyArr = Object.keys(object);
  
  for (const key of keyArr) {
    if (callback(object[key])) {
      // Return the first key for which the callback returns truthy
      return key;
    }
  }
  // If no key is found, return undefined
  return;

};

